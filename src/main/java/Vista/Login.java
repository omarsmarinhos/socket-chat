    private void btnIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {
        String username = txtUsername.getText();
        String password = txtPassword.getText();

        if (username.isBlank()) {
            return;
        }

        if (password.isBlank()) {
            return;
        }

        ClientesRespository clientesRespository = new ClientesRepositoryImpl();
        Optional<ClienteInfo> clienteInfoOpt =  clientesRespository.login(username, password);

        if (clienteInfoOpt.isEmpty()) {
            return;
        }

        try {
            VCliente vCliente = new VCliente(clienteInfoOpt.get());
            vCliente.setVisible(true);
            this.dispose();
        } catch (IOException e) {
            throw new RuntimeException(e);
        }

    }


    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) throws UnknownHostException {//GEN-FIRST:event_btnGuardarActionPerformed
        String username = txtUsername.getText();
        String password1 = txtPassword1.getText();
        String password2 = txtPassword2.getText();
        String nombres = txtNombres.getText();
        String apellidos = txtApellidos.getText();
        String fechaNacimiento = txtFechaNacimiento.getText();
        String celular = txtCelular.getText();



        if (username.isBlank() || password1.isBlank() || password2.isBlank() || nombres.isEmpty() ||
            apellidos.isEmpty() || fechaNacimiento.isEmpty() || celular.isEmpty()) {
            return;
        }

        String ip = InetAddress.getLocalHost().getHostAddress();
        String uuid = java.util.UUID.randomUUID().toString().substring(0, 12).toUpperCase();
        System.out.println(uuid);

        ClienteInfo clienteInfo = new ClienteInfo(ip,uuid, username, 2, password1
                , nombres, apellidos, fechaNacimiento, celular);

        ClientesRespository clientesRespository = new ClientesRepositoryImpl();
        clientesRespository.guardar(clienteInfo);
        JOptionPane.showMessageDialog(this, "Registro exitoso");

        Login login = new Login();
        login.setVisible(true);
        this.dispose();

    }//GEN-LAST:event_btnGuardarActionPerformed

